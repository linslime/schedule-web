<template>
  <div class="login-container">
    <a-form class="loginForm">
      <a-form-item>
        <span class="loginTitle">智班云策-在线排班系统</span>
      </a-form-item>
      <a-form-item v-bind="validateInfos.username">
        <a-input v-model:value="loginModel.username" placeholder="请输入账户">
          <template #prefix>
            <UserOutlined style="color: rgba(0, 0, 0, 0.25)" />
          </template>
        </a-input>
      </a-form-item>
      <a-form-item v-bind="validateInfos.password">
        <a-input v-model:value="loginModel.password" type="password" placeholder="请输入密码">
          <template #prefix>
            <LockOutlined style="color: rgba(0, 0, 0, 0.25)" />
          </template>
        </a-input>
      </a-form-item>
      <a-form-item>
        <a-row :gutter="[16, 16]">
          <a-col :span="12">
            <a-button @click="loginBtn" class="mybtn" type="primary">登录</a-button>
          </a-col>
          <a-col :span="12">
            <a-button @click="resetBtn" class="mybtn">重置</a-button>
          </a-col>
        </a-row>
      </a-form-item>
    </a-form>
  </div>
</template>
<script setup lang='ts'>
import { reactive } from 'vue'
import { setToken, setUserId } from '@/utils/auth'
import { loginApi } from '@/api/login/login'
import useForm from 'ant-design-vue/es/form/useForm';
import { useRouter } from 'vue-router';
const router = useRouter()
const loginModel = reactive({
  username: '',
  password: ''
})
//定义表单验证规则
const rules = reactive({
  username: [{
    required: true,
    trigger: 'change',
    message: '请输入账户'
  }],
  password: [{
    required: true,
    trigger: 'change',
    message: '请输入密码'
  }],
})
//获取表单验证属性
const { validate, validateInfos, clearValidate } = useForm(loginModel, rules)
//登录按钮
const loginBtn = () => {
  validate().then(async () => {
    let res = await loginApi(loginModel)
    if (res && res.code == 200) {
      //存储token
      setToken(res.data.token)
      //存储用户id
      setUserId(res.data.userId)
      //跳转到首页
      router.push({ path: '/data' })
    }
  })
}
//重置按钮
const resetBtn = () => {
  loginModel.username = '';
  loginModel.password = '';
  clearValidate()
}
</script>
